function segText(){
  return (state.segments && state.segments.length) ? state.segments.join(' + ') : '—';
}
function kktCount(){
  return Number(state.kkt?.count || 0);
}
function deviceCounts(){
  // new counters
  const s = Math.max(0, parseInt(state.device_scanner ?? 0, 10) || 0);
  const t = Math.max(0, parseInt(state.device_tsd ?? 0, 10) || 0);
  return {scanners: s, tsd: t};
}
